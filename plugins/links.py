def linkfinder(link):
    blacklist = ["://youtube.com", "://youtu.be", "://www.youtube.com", "://reddit.com", "://www.reddit.com", "://redd.it"];
    try:
        url = link.group(1)
    except AttributeError:
        url = link

    if any(domain in url for domain in blacklist):
        return
    else:
        pass

    unkmsg = "\x02 Link: \x02 {}".format("Image or other non-HTML file.")

    try:
        parser = linkparse.meta(url, agent='mac')
    except linkparse.FormatError:
        return "Link is invalid, or site has timed out."
    try:
        title = parser.title()
    except linkparse.FormatError:
        return unkmsg
    description = parser.description()

    if not description:
            return "\x02 Link: \x02 '{}'".format(title)
    else:
            return "\x02 Link: \x02 '{}' - {}".format(title, description)


@hook.regex(linkre)
def linkmatcher(match, bot):
    switch = bot.config.get("modular", {}).get("autolink")
    if switch == "ON":
        response = linkfinder(match)
        return response

@hook.command
def preview(text):
    response = linkfinder(text)
    return response